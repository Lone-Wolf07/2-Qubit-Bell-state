# This is one of Hello_World programs desgined to test the functionality of qiskit and understand the work flow better

Learned it from Qiskit Youtube channel and was one of the very first programs I wrote 
This is basically something known as a $Bell State$ in Qunatum Computing which leaves us with an Entangled state 

Here we are implementing a 2 qubit Bell state running it locally on a simulator called as the *Aer Simulator* 

In Qiskit we can also run the program on a real quantum hardware, but for this simple code using only 2 qubits a simulator would suffice
